import { Context } from "grammy";

export const helpCommand = async (ctx: Context) => {
  const helpMessage = `
Доступные команды:

/start - Начать работу с ботом
/help - Показать это сообщение помощи
/schedule - Получить расписание занятий

Как использовать бота:
1. Введите /schedule для получения расписания
2. Следуйте инструкциям в сообщениях бота
3. Выберите группу или преподавателя для просмотра расписания

Если у вас возникли проблемы, обратитесь к администратору бота.
 `.trim();

  await ctx.reply(helpMessage);
};